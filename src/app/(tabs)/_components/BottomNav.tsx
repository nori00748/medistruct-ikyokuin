"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";

const tabs = [
  { href: "/", label: "ホーム", icon: HomeIcon },
  { href: "/calendar", label: "カレンダー", icon: CalendarIcon },
  { href: "/kibou", label: "希望休", icon: KibouIcon },
  { href: "/settings", label: "設定", icon: SettingsIcon },
] as const;

export function BottomNav() {
  const pathname = usePathname();

  return (
    <nav
      className="fixed bottom-0 left-0 right-0 max-w-[420px] mx-auto bg-surface border-t border-border px-2 py-1.5 grid grid-cols-4 gap-1"
      style={{ boxShadow: "0 -1px 4px rgba(15,23,42,.04)" }}
    >
      {tabs.map((tab) => {
        const isActive = pathname === tab.href;
        return (
          <Link
            key={tab.href}
            href={tab.href}
            className="flex flex-col items-center justify-center py-1.5 transition-colors"
            style={{ color: isActive ? "#2563eb" : "#94a3b8" }}
          >
            <tab.icon />
            <span className="text-[10px] font-semibold mt-0.5">{tab.label}</span>
          </Link>
        );
      })}
    </nav>
  );
}

